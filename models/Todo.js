import mongoose from 'mongoose';

const TodoSchema = new mongoose.Schema(
  // дополнить
  {
    title: {
      type: String,
      required: true,
    },
    text: {
      type: String,
      required: true,
      unique: true,
    },
    isComplete: {
      type: Boolean,
      default: false,
    },
    user: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'User',
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

export const TodoModel = mongoose.model('Todo', TodoSchema);
